    private void actualizarTabla() {
        try {
            List<DosisDTO> lista = dao.readALL();

            String matriz[][] = new String[lista.size()][6];

            for (int i = 0; i < lista.size(); i++) {
                matriz[i][0] = lista.get(i).getId()+"";
                matriz[i][1] = lista.get(i).getNombrePersona();
                matriz[i][2] = lista.get(i).getEdad()+"";
                matriz[i][3] = lista.get(i).getVacuna();
                matriz[i][4] = lista.get(i).getNumDosis()+"";
                matriz[i][5] = lista.get(i).getFecha();
            }

            jTable1.setModel(new javax.swing.table.DefaultTableModel(
                    matriz,
                    new String[]{
                        "ID", "Nombre", "Edad","Tipo vacuna","N dosis suministrada","Fecha de registro"
                    }
            ));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
    private void actualizarComboBox()
    {
        try {
            VacunaDAO dao = new VacunaDAO();
            List<VacunaDTO> lista = dao.readALL();
            for (int i = 0; i < lista.size(); i++) {
                jComboBox1.addItem(lista.get(i).getNombre());
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
