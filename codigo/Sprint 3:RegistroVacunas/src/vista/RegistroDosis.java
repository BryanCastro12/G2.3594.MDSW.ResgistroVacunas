            // TODO add your handling code here:
            DateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            String fecha = date.format(jDateChooser1.getDate());
            dto.setNombrePersona(jTextField1.getText());
            dto.setEdad(Integer.parseInt(jTextField2.getText()));
            dto.setVacuna(jComboBox1.getSelectedItem().toString());
